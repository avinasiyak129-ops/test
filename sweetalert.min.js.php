function getPrimaryKey($table) {
    $map = [
        'rasvehicleregdtls_tbl' => 'rasvehicleregdtls_pk',
        'ivmsvehicleregdtls_tbl' => 'ivmsvehicleregdtls_pk',
        'learnercarddtls_tbl' => 'learnercarddtls_pk'
    ];
    return $map[$table];
}

/* ===========================
   LOAD FK DATA
=========================== */
function loadOptions($pdo, $table, $pk) {
    return $pdo->query("SELECT $pk FROM $table LIMIT 50")->fetchAll(PDO::FETCH_COLUMN);
}

